const regex = /^(?!.*[Aa]).{6,}$/;


// Приклади рядків
const strings = [
    "abcdef",     // Задовольняє умові
    "Abcdefg",    // Не задовольняє умові (містить 'A')
    "1234567",    // Задовольняє умові
    "a12345",     // Не задовольняє умові (менше шести символів)
    "aabbcc",     // Не задовольняє умові (менше шести унікальних символів)
    "a1b2c3",     // Задовольняє умові
    "AaAaAa"      // Не задовольняє умові (всі символи 'A')
];

// Перевірка рядків за допомогою регулярного виразу
strings.forEach(str => {
    if (regex.test(str)) {
        console.log(`"${str}" задовольняє умові.`);
    } else {
        console.log(`"${str}" не задовольняє умові.`);
    }
});
